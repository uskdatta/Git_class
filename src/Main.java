public class Main {
    public static void main(String[] args) {
        int[] numbers = {10, 23, 45, 66, 78, 91};

        for (int number : numbers) {
            if (number % 2 == 0) {
                System.out.println(number + " is even.");
            } else {
                System.out.println(number + " is odd.");
            }
        }
    }
}
